#include <stdio.h>

//operators carry out simpl arithmetics

// +  @addition
// -  @subtraction
// *  @multiplication
// /  @division
// %  @modulus
// ++ @increment
// — @decrement

int main()
{
	int a,b,c,d,e;
	 a = 40;
	 b = 25;
	
	 c = a + b;
	 d = b - a;
	 e = a % d;
	
	printf("%d\n", c);
	printf("\n");
	printf("%d\n",d);
	printf("\n");
	printf("%d\n",e);
	
	return 0;
	}
